package com.taofen8.mid.kong.admin.response;


import com.taofen8.mid.kong.util.JsonUtil;

public class SNIResp extends BaseResp {

  private String id;
  private String name;
  private CertificateResp certificate;
  private Long created_at;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public CertificateResp getCertificate() {
    return certificate;
  }

  public void setCertificate(CertificateResp certificate) {
    this.certificate = certificate;
  }

  public Long getCreated_at() {
    return created_at;
  }

  public void setCreated_at(Long created_at) {
    this.created_at = created_at;
  }

  @Override
  public String toString() {
    String header = super.toString();

    StringBuilder builder = new StringBuilder();
    builder.append(header);
    builder.append("\n");
    builder.append(JsonUtil.toJSONString(this));
    return builder.toString();
  }
}
